
public class OperadoresRelacionais {

	public static void main(String[] args) {
		
		int nota1 = 90;
		int nota2 = 90;
		
		// == -> Igual
		// != -> Diferente
		// <  -> Menor que
		// >  -> Maior que
		// <= -> Menor ou Igual
		// >= -> Maior ou igual
		
		if(nota1 == nota2) {
			System.out.println("As notas são iguais");
		} else {
			System.out.println("As notas são diferentes");
		}
	}

}
