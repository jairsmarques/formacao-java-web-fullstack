
public class Operadoresrelacionais {

	public static void main(String[] args) {

		// == -> Igual
		// != -> Diferente
		// <  -> Menor que
		// >  -> Maior que
		// <= -> Menor ou Igual
		// >= -> maior ou Igual

		int nota1 = 90;
		int nota2 = 90;

		if (nota1 == nota2) {
			System.out.println("As notas são iguais");
		} else {
			System.out.println("As notas são diferentes");
		}

	}

}
