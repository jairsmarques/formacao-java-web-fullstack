
public class OperacaoRelacionais {

	public static void main(String[] args) {
		// Igual         ->   ==
		// Diferente     ->   !=
		// Menor         ->   <
		//Maior          ->   >
		//Menor ou igual ->   <=
		//Maior ou igual ->   >=
		
		int nota1 = 90;
		int nota2 = 90;
		
		if (nota1 == nota2) {
			System.out.println("As notas são iguais");
		} else {
			System.out.println("As notas são diferentes");
		}

	}

}
